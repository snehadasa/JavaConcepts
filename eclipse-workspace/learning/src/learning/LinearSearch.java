package learning;

public class LinearSearch {

	public static int LinearSearch(int[] arr, int x) {
		for (int i = 0; i <= arr.length; i++) {
			if(arr[i] == x) {
				return i;
			}
		}
		return -1;
	}
	
	public static void main(String[] args) {
		int[] arr = {4, 6, 8, 1, 10, 2};
		int x = 10;
		int res = LinearSearch(arr, x);
		System.out.println(res);
	}

}
